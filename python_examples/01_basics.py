# Basic Python Concepts
# This file covers fundamental Python concepts with detailed explanations

# Variables and Data Types
name = "John Doe"  # String variable - stores text
age = 25          # Integer variable - stores whole numbers
height = 1.75     # Float variable - stores decimal numbers
is_student = True # Boolean variable - stores True or False

# Basic arithmetic operations
sum = 10 + 5      # Addition
difference = 10 - 5  # Subtraction
product = 10 * 5    # Multiplication
quotient = 10 / 5   # Division (returns float)
integer_division = 10 // 3  # Floor division (returns integer)
remainder = 10 % 3  # Modulus (remainder after division)

# String operations
first_name = "John"
last_name = "Doe"
full_name = first_name + " " + last_name  # String concatenation
greeting = f"Hello, {full_name}!"  # f-string formatting 